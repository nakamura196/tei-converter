/* ======== TEI Gallery ========
   A two-pane launcher: pick an XML document (left) and an XSL stylesheet
   (right), then open that combination full-screen in view.html.

   Document / stylesheet metadata (description, language, category,
   license) is NOT hand-maintained here — it is read from catalog.json,
   generated by scripts/build-catalog.mjs from the teiHeader of each
   XML and the leading comment of each XSL. XSL_CATALOG (common.js) still
   provides the routing (id / xsl path / input type). */

/* ---- State ---- */
let catalog     = { xml: [], xsl: [] };  // loaded from catalog.json
let addedXmls   = [];     // user-added entries (scope: 'added')
let selectedXml = null;
let selectedXsl = null;
let addCounter  = 0;

/* ---- Elements ---- */
const xmlListEl  = document.getElementById('xmlList');
const xslListEl  = document.getElementById('xslList');
const xmlCountEl = document.getElementById('xmlCount');
const selXmlEl   = document.getElementById('selXml');
const selXslEl   = document.getElementById('selXsl');
const openBtn    = document.getElementById('pubOpenBtn');
const urlInput   = document.getElementById('xmlUrlInput');
const urlBtn     = document.getElementById('xmlUrlBtn');
const fileBtn    = document.getElementById('xmlFileBtn');
const fileInput  = document.getElementById('xmlFileInput');

/* Inline "open in a new tab" icon (box with an out-arrow). */
const OPEN_ICON =
  '<svg class="pub-open-icon" viewBox="0 0 16 16" width="11" height="11" aria-hidden="true">' +
    '<path fill="currentColor" d="M9 1a1 1 0 0 0 0 2h2.59L6.3 8.29a1 1 0 1 0 1.42 1.42L13 4.41V7a1 1 0 1 0 2 0V2a1 1 0 0 0-1-1H9zM3.5 3A2.5 2.5 0 0 0 1 5.5v7A2.5 2.5 0 0 0 3.5 15h7a2.5 2.5 0 0 0 2.5-2.5V9a1 1 0 1 0-2 0v3.5a.5.5 0 0 1-.5.5h-7a.5.5 0 0 1-.5-.5v-7a.5.5 0 0 1 .5-.5H7a1 1 0 0 0 0-2H3.5z"/>' +
  '</svg>';

/* The view link's inner markup: icon + label (e.g. "XML を見る"). */
function viewContent(labelKey) { return OPEN_ICON + '<span>' + esc(t(labelKey)) + '</span>'; }

function scopeLabel(scope) {
  return scope === 'bundled' ? t('pubScopeBundled')
       : scope === 'remote'  ? t('pubScopeRemote')
       : t('pubScopeAdded');
}

/* Build the chip row shown under the description: category + languages. */
function metaRow(entry) {
  const chips = [];
  if (entry.category) {
    chips.push('<span class="pub-cat">' + esc(entry.category) + '</span>');
  }
  (entry.languages || []).forEach(code => {
    chips.push('<span class="pub-lang">' + esc(String(code).toUpperCase()) + '</span>');
  });
  return chips.length ? '<div class="pub-item-meta">' + chips.join('') + '</div>' : '';
}

/* License line (TEI publicationStmt/availability). */
function licenseRow(entry) {
  return entry.license
    ? '<p class="pub-license"><span class="pub-license-label">' +
        esc(t('pubLicense')) + '</span> ' + esc(entry.license) + '</p>'
    : '';
}

/* "View" link — opens the .xml / .xsl in a new browser tab.
   No `download` attribute: the browser shows the file directly. */
function viewLink(url, labelKey) {
  return '<div class="pub-item-actions">' +
    '<a class="btn-secondary pub-open" href="' + esc(url) + '" ' +
      'target="_blank" rel="noopener">' + viewContent(labelKey) + '</a>' +
  '</div>';
}

/* ======== Rendering ======== */
function allXmls() { return catalog.xml.concat(addedXmls); }

function renderXmlList() {
  const xmls = allXmls();
  xmlCountEl.textContent = '(' + xmls.length + ')';
  xmlListEl.innerHTML = '';
  xmls.forEach(entry => {
    const li = document.createElement('li');
    li.className = 'pub-item' +
      (selectedXml && selectedXml.id === entry.id ? ' selected' : '');
    li.dataset.id = entry.id;
    li.innerHTML =
      '<div class="pub-item-head">' +
        '<span class="pub-item-title">' + esc(entry.title || entry.url) + '</span>' +
        '<span class="pub-badge pub-badge-' + entry.scope + '">' +
          esc(scopeLabel(entry.scope)) + '</span>' +
      '</div>' +
      (entry.description
        ? '<p class="pub-item-note">' + esc(entry.description) + '</p>' : '') +
      metaRow(entry) +
      licenseRow(entry) +
      viewLink(entry.url, 'pubViewXml');
    li.addEventListener('click', e => {
      if (e.target.closest('.pub-open')) return;   // let the open link work
      selectedXml = entry; refresh();
    });
    xmlListEl.appendChild(li);
  });
}

function renderXslList() {
  xslListEl.innerHTML = '';
  xslEntries().forEach(entry => {
    const li = document.createElement('li');
    li.className = 'pub-item' +
      (selectedXsl && selectedXsl.id === entry.id ? ' selected' : '');
    li.dataset.id = entry.id;
    const badge = entry.input === 'folder' ? t('exInputFolder') : t('exInputFile');
    li.innerHTML =
      '<div class="pub-item-head">' +
        '<span class="pub-item-title">' + esc(entry.title) + '</span>' +
        '<span class="pub-badge">' + esc(badge) + '</span>' +
      '</div>' +
      (entry.description
        ? '<p class="pub-item-note">' + esc(entry.description) + '</p>' : '') +
      metaRow(entry) +
      licenseRow(entry) +
      viewLink(entry.xsl, 'pubViewXsl');
    li.addEventListener('click', e => {
      if (e.target.closest('.pub-open')) return;
      selectedXsl = entry; refresh();
    });
    xslListEl.appendChild(li);
  });
}

/* XSL routing (XSL_CATALOG, common.js) merged with file-derived
   metadata (catalog.json, keyed by xsl path). */
function xslEntries() {
  const byUrl = {};
  catalog.xsl.forEach(x => { byUrl[x.url] = x; });
  return XSL_CATALOG.map(c => {
    const m = byUrl[c.xsl] || {};
    return {
      id: c.id, xsl: c.xsl, input: c.input,
      title:       m.title || (c.title && (c.title[lang] || c.title.ja)) || c.id,
      description: m.description || '',
      category:    m.category || '',
      license:     m.license || '',
    };
  });
}

/* Reflect a step's state on its pane / bar: 'done' (selected),
   'active' (do this now), '' (pending). Highlights the block the
   user should act on next. */
function setStep(name, state) {
  const el = document.querySelector('[data-step="' + name + '"]');
  if (!el) return;
  el.classList.toggle('done',   state === 'done');
  el.classList.toggle('active', state === 'active');
}

/* Update selection highlight, the step guide, the selection bar, and the button. */
function refresh() {
  document.querySelectorAll('#xmlList .pub-item').forEach(li =>
    li.classList.toggle('selected', !!selectedXml && li.dataset.id === selectedXml.id));
  document.querySelectorAll('#xslList .pub-item').forEach(li =>
    li.classList.toggle('selected', !!selectedXsl && li.dataset.id === selectedXsl.id));

  selXmlEl.textContent = selectedXml ? (selectedXml.title || selectedXml.url) : t('pubNone');
  selXmlEl.classList.toggle('pub-none', !selectedXml);
  selXslEl.textContent = selectedXsl ? selectedXsl.title : t('pubNone');
  selXslEl.classList.toggle('pub-none', !selectedXsl);

  const ready = !!(selectedXml && selectedXsl);
  setStep('xml',  selectedXml ? 'done' : 'active');
  setStep('xsl',  selectedXsl ? 'done' : (selectedXml ? 'active' : ''));
  setStep('open', ready ? 'active' : '');

  openBtn.disabled = !ready;
}

/* ======== Open full-screen (in a new tab) ======== */
openBtn.addEventListener('click', () => {
  if (!selectedXml || !selectedXsl) return;
  const url = 'view.html?url=' + encodeURIComponent(selectedXml.url) +
    '&xsl=' + encodeURIComponent(selectedXsl.id);
  window.open(url, '_blank', 'noopener');
});

/* ======== Add an XML ======== */
function addXmlEntry(url, title) {
  const entry = {
    id: 'added-' + (++addCounter), scope: 'added', url: url,
    title: title, description: '', languages: [], category: '', license: '',
  };
  addedXmls.push(entry);
  selectedXml = entry;
  renderXmlList();
  refresh();
}

urlBtn.addEventListener('click', () => {
  hideError();
  const url = urlInput.value.trim();
  if (!url) { showError(t('errXmlUrl')); return; }
  addXmlEntry(url, url.split('/').pop() || url);
  urlInput.value = '';
});
urlInput.addEventListener('keydown', e => { if (e.key === 'Enter') urlBtn.click(); });

fileBtn.addEventListener('click', () => fileInput.click());
fileInput.addEventListener('change', () => {
  const file = fileInput.files && fileInput.files[0];
  if (file) {
    // A blob: URL works across same-origin pages within this session,
    // so view.html can fetch it. (Relative <graphic url> images won't
    // resolve for an uploaded single file — fine for non-image XSLs.)
    addXmlEntry(URL.createObjectURL(file), file.name);
  }
  fileInput.value = '';
});

/* ======== Init ======== */
function renderAll() { renderXmlList(); renderXslList(); refresh(); }

fetch('catalog.json')
  .then(res => { if (!res.ok) throw new Error('HTTP ' + res.status); return res.json(); })
  .then(data => { catalog = data; renderAll(); })
  .catch(() => { showError(t('errCatalog')); renderAll(); });

// Re-render localized labels (badges/links) after the language toggle.
document.getElementById('langBtn').addEventListener('click', renderAll);
